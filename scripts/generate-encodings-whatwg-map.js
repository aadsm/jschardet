#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { chardetVersion } from './chardet-version.js';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const testSpecPath = join(root, 'chardet', 'tests', 'test_spec_whatwg.py');
const outputPath = join(root, 'src', 'encoding-whatwg-map.ts');

function parseWhatwgToChardet(src) {
  const result = {};
  let inDict = false;

  for (const line of src.split('\n')) {
    if (!inDict && line.includes('_WHATWG_TO_CHARDET') && line.includes('=')) {
      inDict = true;
      continue;
    }
    if (!inDict) continue;
    if (line.trim() === '}') { inDict = false; continue; }

    const m = line.match(/"([^"]+)":\s*frozenset\(\{([^}]+)\}\)/);
    if (m) {
      const group = m[1];
      const canonicals = [...m[2].matchAll(/"([^"]+)"/g)].map(x => x[1]);
      result[group] = canonicals;
    }
  }

  return result;
}

export function generate() {
  const src = readFileSync(testSpecPath, 'utf8');
  const whatwgToChardet = parseWhatwgToChardet(src);

  // Invert: chardet canonical → WHATWG label (group name lowercased is a valid WHATWG label)
  const chardetToWhatwg = {};
  for (const [group, canonicals] of Object.entries(whatwgToChardet)) {
    const label = group.toLowerCase();
    for (const canonical of canonicals) {
      chardetToWhatwg[canonical] = label;
    }
  }

  const entries = Object.entries(chardetToWhatwg)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([canonical, label]) => `  ${JSON.stringify(canonical)}: ${JSON.stringify(label)},`)
    .join('\n');

  const version = chardetVersion();
  writeFileSync(
    outputPath,
    `// generated by scripts/generate-encodings-whatwg-map.js — do not edit manually\n` +
    `// chardet ${version}\n` +
    `export const ENCODING_WHATWG_MAP: Readonly<Record<string, string>> = Object.freeze({\n` +
    `${entries}\n` +
    `});\n`,
    'utf8',
  );

  console.log(`Wrote ${Object.keys(chardetToWhatwg).length} entries to src/encoding-whatwg-map.ts`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generate();
}
