#!/usr/bin/env node
import { readFileSync, writeFileSync, unlinkSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { dirname, join } from 'node:path';
import { tmpdir } from 'node:os';
import { inflateSync, deflateSync, constants as zlibConstants } from 'node:zlib';
import { buildSync } from 'esbuild';
import { chardetVersion } from './chardet-version.js';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const chardetModelsDir = join(root, 'chardet', 'src', 'chardet', 'models');
const outputDir = join(root, 'src', 'models');
const browserDecoderPath = join(root, 'src', 'runtime', 'decompress.browser.ts');

const SCRIPT_NAME = 'generate-model-bins.js';
const V2_MAGIC = Buffer.from('CMD2');

const MODE_COMPRESSED = 'compressed';
const MODE_RAW = 'raw';

// Re-pack chardet's models.bin into a fully-decompressed CMD2 image: identical
// header layout, but the trailing bigram blob is inflated in place. The
// compressed-mode emitter then re-zips this whole image with our own Z_FIXED
// encoder so a single decompressor on the consume side handles all three
// .bin payloads uniformly.
function rebuildModelsBinRaw() {
  const data = readFileSync(join(chardetModelsDir, 'models.bin'));
  if (data.subarray(0, 4).compare(V2_MAGIC) !== 0) {
    throw new Error('models.bin: missing CMD2 magic');
  }
  let offset = 4;
  const numModels = data.readUInt32BE(offset);
  offset += 4;
  if (numModels > 10_000) {
    throw new Error(`models.bin: num_models=${numModels} exceeds limit`);
  }
  for (let i = 0; i < numModels; i++) {
    const nameLen = data.readUInt32BE(offset);
    offset += 4;
    if (nameLen > 256) throw new Error(`models.bin: name_len=${nameLen} exceeds 256`);
    offset += nameLen + 8;
  }
  const header = data.subarray(0, offset);
  const blob = inflateSync(data.subarray(offset));
  const expected = numModels * 65536;
  if (blob.length !== expected) {
    throw new Error(`models.bin: decompressed size ${blob.length} != expected ${expected}`);
  }
  return { bytes: Buffer.concat([header, blob]), numModels };
}

// Compress with the fixed-Huffman strategy. Z_FIXED forces every block to be a
// fixed-Huffman block, which lets the browser-side decoder skip dynamic-table
// reconstruction (the hard part of DEFLATE).
function compress(bytes) {
  return deflateSync(bytes, {
    level: 9,
    strategy: zlibConstants.Z_FIXED,
    windowBits: 15,
    memLevel: 8,
  });
}

// Wrapper-body templates. Each template's .toString() output is the readBytes
// function that gets baked into the generated wrapper, so authoring them as
// real (named) JS functions keeps them syntax-checked and IDE-navigable. They
// are never called inside this script — only stringified.
//
// `cached`, `data`, `decompress`, `Buffer`, `atob` are referenced only inside
// the stringified bodies. They are not in scope here; ESLint with no-undef
// would complain — disable for this section if/when ESLint is added. Function
// names are renamed to readBytes during emit so the wrapper exports the right
// symbol.

/* eslint-disable no-undef */

/** @returns {Uint8Array} */
function readBytesCompressed() {
  if (cached) return cached;
  const raw = typeof Buffer !== 'undefined'
    ? new Uint8Array(Buffer.from(data, 'base64'))
    : Uint8Array.from(atob(data), c => c.charCodeAt(0));
  cached = decompress(raw);
  return cached;
}

/** @returns {Uint8Array} */
function readBytesRaw() {
  if (cached) return cached;
  cached = typeof Buffer !== 'undefined'
    ? new Uint8Array(Buffer.from(data, 'base64'))
    : Uint8Array.from(atob(data), c => c.charCodeAt(0));
  return cached;
}

/* eslint-enable no-undef */

function emitWrapper(filePath, bytes, version, mode) {
  const b64 = bytes.toString('base64');
  const template = mode === MODE_COMPRESSED ? readBytesCompressed : readBytesRaw;
  const fnSource = template.toString().replace(/^function \w+/, 'function readBytes');
  const importLine = mode === MODE_COMPRESSED
    ? `import { decompress } from '../runtime/decompress.js';\n\n`
    : '';

  const content =
    `// generated by scripts/${SCRIPT_NAME} — do not edit manually\n` +
    `// chardet ${version}\n` +
    `// mode: ${mode}\n` +
    importLine +
    `const data = ${JSON.stringify(b64)};\n` +
    `\n` +
    `let cached = null;\n` +
    `\n` +
    `/** @returns {Uint8Array} */\n` +
    `export ${fnSource}\n`;
  writeFileSync(filePath, content, 'utf8');
}

function bytesEqual(a, b) {
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return false;
  return true;
}

// Detect generation mode from the wrapper's header line so verify() can work
// against whichever form is currently on disk.
function detectMode(filePath) {
  const head = readFileSync(filePath, 'utf8').slice(0, 256);
  const m = /\/\/ mode: (compressed|raw)/.exec(head);
  return m ? m[1] : null;
}

// Compile the browser decoder to a temp ESM module so verify() can exercise it
// from plain Node. esbuild is a build-time dep; the produced bundle is only
// used for this round-trip check.
async function loadBrowserDecompress() {
  const tempPath = join(tmpdir(), `decompress-browser-${process.pid}-${Date.now()}.mjs`);
  buildSync({
    entryPoints: [browserDecoderPath],
    bundle: true,
    format: 'esm',
    outfile: tempPath,
    platform: 'neutral',
    logLevel: 'silent',
  });
  try {
    const mod = await import(pathToFileURL(tempPath).href);
    return mod.decompress;
  } finally {
    try { unlinkSync(tempPath); } catch { /* ignore */ }
  }
}

export async function generate(mode = MODE_COMPRESSED) {
  if (mode !== MODE_COMPRESSED && mode !== MODE_RAW) {
    throw new Error(`unknown mode: ${mode}`);
  }
  const version = chardetVersion();
  const maybeCompress = (bytes) => mode === MODE_COMPRESSED ? compress(bytes) : bytes;

  console.log(`Generating src/models/models.bin.js (${mode}) ...`);
  const { bytes: modelsRaw, numModels } = rebuildModelsBinRaw();
  const modelsPayload = maybeCompress(modelsRaw);
  emitWrapper(join(outputDir, 'models.bin.js'), modelsPayload, version, mode);
  console.log(`  ${numModels} models, ${modelsRaw.length.toLocaleString()} raw → ${modelsPayload.length.toLocaleString()} payload bytes`);

  console.log(`Generating src/models/idf.bin.js (${mode}) ...`);
  const idfRaw = readFileSync(join(chardetModelsDir, 'idf.bin'));
  if (idfRaw.length !== 65536) {
    throw new Error(`idf.bin: expected 65536 bytes, got ${idfRaw.length}`);
  }
  const idfPayload = maybeCompress(idfRaw);
  emitWrapper(join(outputDir, 'idf.bin.js'), idfPayload, version, mode);
  console.log(`  ${idfRaw.length.toLocaleString()} raw → ${idfPayload.length.toLocaleString()} payload bytes`);

  console.log(`Generating src/models/confusion.bin.js (${mode}) ...`);
  const confusionRaw = readFileSync(join(chardetModelsDir, 'confusion.bin'));
  const confusionPayload = maybeCompress(confusionRaw);
  emitWrapper(join(outputDir, 'confusion.bin.js'), confusionPayload, version, mode);
  console.log(`  ${confusionRaw.length.toLocaleString()} raw → ${confusionPayload.length.toLocaleString()} payload bytes`);

  console.log('Verifying parity with chardet source ...');
  return verify();
}

// Integrity check: each .bin.js wrapper, when its readBytes() is invoked, must
// produce the same bytes as the chardet source. In compressed mode, also feed
// the raw compressed payload through the browser-side JS decoder and confirm
// it produces the same bytes — this is the build-time correctness gate for
// the browser DEFLATE decoder. Catches base64 encode/decode bugs, header
// reframing bugs, file corruption, and stale wrappers.
export async function verify() {
  const expected = {
    'models.bin.js': rebuildModelsBinRaw().bytes,
    'idf.bin.js': readFileSync(join(chardetModelsDir, 'idf.bin')),
    'confusion.bin.js': readFileSync(join(chardetModelsDir, 'confusion.bin')),
  };
  const errors = [];

  // Per-wrapper Node-path round-trip.
  const wrapperModes = {};
  for (const [name, want] of Object.entries(expected)) {
    const wrapperPath = join(outputDir, name);
    const mode = detectMode(wrapperPath);
    if (mode === null) {
      errors.push(`${name}: missing or unrecognised mode header`);
      continue;
    }
    wrapperModes[name] = mode;
    let got;
    try {
      const mod = await import(`${pathToFileURL(wrapperPath).href}?t=${Date.now()}`);
      got = mod.readBytes();
    } catch (e) {
      errors.push(`${name}: failed to import — ${e.message}`);
      continue;
    }
    if (!bytesEqual(got, want)) {
      errors.push(
        `${name}: readBytes() bytes do not match chardet source ` +
        `(got ${got.length} bytes, expected ${want.length})`,
      );
    }
  }

  // Browser-decoder round-trip: only meaningful for compressed wrappers.
  const compressedWrappers = Object.entries(wrapperModes).filter(([, m]) => m === MODE_COMPRESSED);
  if (compressedWrappers.length > 0) {
    let browserDecompress;
    try {
      browserDecompress = await loadBrowserDecompress();
    } catch (e) {
      errors.push(`browser decoder: failed to compile — ${e.message}`);
    }
    if (browserDecompress) {
      for (const [name] of compressedWrappers) {
        // Re-derive the payload by re-running the encoder so we feed the
        // decoder the same bytes the wrapper carries.
        const payload = compress(expected[name]);
        let got;
        try {
          got = browserDecompress(payload);
        } catch (e) {
          errors.push(`${name}: browser decoder threw — ${e.message}`);
          continue;
        }
        if (!bytesEqual(got, expected[name])) {
          errors.push(
            `${name}: browser decoder output does not match chardet source ` +
            `(got ${got.length} bytes, expected ${expected[name].length})`,
          );
        }
      }
    }
  }

  if (errors.length > 0) {
    console.error('Verification failed:');
    for (const e of errors) console.error('  - ' + e);
    process.exit(1);
  }
  const browserCount = compressedWrappers.length;
  const summary = browserCount > 0
    ? `Verified: 3 wrappers via Node + ${browserCount} via browser decoder.`
    : 'Verified: 3 wrappers match chardet source bytes.';
  console.log(summary);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const args = process.argv.slice(2);
  let action = '--generate';
  let mode = MODE_COMPRESSED;
  for (const a of args) {
    if (a === '--generate' || a === '--verify') action = a;
    else if (a === '--raw') mode = MODE_RAW;
    else if (a === '--compressed') mode = MODE_COMPRESSED;
    else {
      console.error(`Usage: node scripts/${SCRIPT_NAME} [--generate|--verify] [--raw|--compressed]`);
      process.exit(1);
    }
  }
  if (action === '--generate') {
    await generate(mode);
  } else {
    await verify();
  }
}
