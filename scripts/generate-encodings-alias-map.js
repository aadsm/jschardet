#!/usr/bin/env node
import { spawnSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const outputPath = join(root, 'src', 'encoding-alias-map.ts');

const py = `\
import json, codecs, sys, encodings, encodings.aliases as ea
import pkgutil

result = {}

# Static alias dict (covers the majority of names)
for alias in ea.aliases:
    try:
        result[alias] = codecs.lookup(alias).name
    except LookupError:
        pass

# Module-import fallback: codecs.lookup() also resolves names by importing
# encodings.<normalized> directly, which covers names like "latin_1" and
# "iso2022_jp_2" that are not in the static alias dict.
for _importer, modname, ispkg in pkgutil.iter_modules(encodings.__path__):
    if ispkg:
        continue
    try:
        result[modname] = codecs.lookup(modname).name
    except LookupError:
        pass

version = f"{sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}"
print(json.dumps({"version": version, "aliases": result}))
`;

export function generate() {
  const { stdout, stderr, status } = spawnSync('python3', ['-'], {
    input: py,
    encoding: 'utf8',
  });

  if (status !== 0) {
    process.stderr.write(stderr);
    process.exit(1);
  }

  const { version, aliases } = JSON.parse(stdout.trim());
  const entries = Object.entries(aliases)
    .map(([alias, canonical]) => `  ${JSON.stringify(alias)}: ${JSON.stringify(canonical)},`)
    .join('\n');

  writeFileSync(
    outputPath,
    `// generated by scripts/generate-encodings-alias-map.js — do not edit manually\n` +
    `// python ${version}\n` +
    `export const ENCODING_ALIAS_MAP: Readonly<Record<string, string>> = Object.freeze({\n` +
    `${entries}\n` +
    `});\n`,
    'utf8',
  );

  console.log(`Wrote ${Object.keys(aliases).length} aliases to src/encoding-alias-map.ts`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generate();
}
