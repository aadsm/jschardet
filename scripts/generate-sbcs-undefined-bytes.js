#!/usr/bin/env node
// Emits src/sbcs-undefined-bytes.ts — for each SBCS in the chardet registry
// that has codepoint gaps under Python's strict-decode, the set of bytes that
// raise UnicodeDecodeError. Used by validity filtering for encodings that
// have no WHATWG label (where TextDecoder cannot do the check). See
// See src/sbcs-undefined-bytes.ts.
import { spawnSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import { chardetVersion } from './chardet-version.js';

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const outputPath = join(root, 'src', 'sbcs-undefined-bytes.ts');
const chardetSrc = join(root, 'chardet', 'src');

// UTF and ASCII variants are flagged is_multibyte=False in chardet's registry
// but are handled by deterministic pipeline stages (BOM, UTF-1632, escape,
// ascii, utf8) before validity. Excluding them keeps the table focused on the
// SBCS-byte-validity case this script addresses.
const py = `
import json, codecs, sys
sys.path.insert(0, ${JSON.stringify(chardetSrc)})
from chardet.registry import REGISTRY

SKIP = {
    "ascii", "utf-7",
    "utf-8", "utf-8-sig",
    "utf-16", "utf-16-be", "utf-16-le",
    "utf-32", "utf-32-be", "utf-32-le",
}

result = {}
missing = []
for enc in REGISTRY.values():
    if enc.is_multibyte: continue
    if enc.name in SKIP: continue
    try:
        codecs.lookup(enc.name)
    except LookupError:
        missing.append(enc.name)
        continue
    bad = []
    for b in range(256):
        try:
            bytes([b]).decode(enc.name, errors='strict')
        except UnicodeDecodeError:
            bad.append(b)
    if bad:
        result[enc.name] = bad

print(json.dumps({"version": f"{sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}", "missing": missing, "data": result}))
`;

export function generate() {
  const { stdout, stderr, status } = spawnSync('python3', ['-'], {
    input: py,
    encoding: 'utf8',
  });

  if (status !== 0) {
    process.stderr.write(stderr);
    process.exit(1);
  }

  const { version, missing, data } = JSON.parse(stdout.trim());

  for (const name of missing) {
    process.stderr.write(`warning: SBCS '${name}' missing from Python codecs; skipping\n`);
  }

  const entries = Object.entries(data)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([name, bytes]) => {
      const hex = bytes.map(b => `0x${b.toString(16).padStart(2, '0')}`).join(', ');
      return `  ${JSON.stringify(name)}: new Set([${hex}]),`;
    })
    .join('\n');

  const chardetVer = chardetVersion();
  writeFileSync(
    outputPath,
    `// generated by scripts/generate-sbcs-undefined-bytes.js — do not edit manually\n` +
    `// chardet ${chardetVer}, python ${version}\n` +
    `export const SBCS_UNDEFINED_BYTES: Readonly<Record<string, ReadonlySet<number>>> = Object.freeze({\n` +
    `${entries}\n` +
    `});\n`,
    'utf8',
  );

  console.log(`Wrote ${Object.keys(data).length} entries to src/sbcs-undefined-bytes.ts`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generate();
}
